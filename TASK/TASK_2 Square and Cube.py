# Python script that calculates the square and cube of a given number.
# num = 2 sq - 4, c = 8

# Define the number
num = 2

# Calculate the square
square = num ** 2

# Calculate the cube
cube = num ** 3

# Print the results
print(f"The square of {num} is {square}")
print(f"The cube of {num} is {cube}")



